    // Bu method her işlem butonuna basıldığında çalışacak
public void islemYap(String yeniIslem) {
    if (!txtEkran.getText().equals("")) { // Ekran boş değilse işlem yap
        
        if (islem == null || islem.isEmpty()) {
            // 1. Durum: İlk defa sayı giriliyor (Henüz işlem yok)
            sayi1 = Double.parseDouble(txtEkran.getText());
        } 
        else {
            // 2. Durum: Zincirleme işlem (Örn: 5 + 3 dedikten sonra -'ye bastın)
            // Önceki işlemi tamamlayıp sonucu sayi1'e atıyoruz
            double geciciSayi = Double.parseDouble(txtEkran.getText());
            
            switch (islem) {
                case "+": sayi1 = sayi1 + geciciSayi; break;
                case "-": sayi1 = sayi1 - geciciSayi; break;
                case "x": sayi1 = sayi1 * geciciSayi; break;
                case "/": sayi1 = sayi1 / geciciSayi; break;
            }
            // Ara sonucu görmek istersen konsola yazdırabilirsin
            System.out.println("Ara Sonuc: " + sayi1); 
        }
        
        // Yeni basılan işlemi (örneğin -) hafızaya al
        islem = yeniIslem;
        
        // Ekranı temizle ki yeni sayıyı girebilesin
        txtEkran.setText("");
    }
}

