    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        // TODO add your handling code here:
        if (islem != null && !txtEkran.getText().equals("")) {
    sayi2 = Double.parseDouble(txtEkran.getText());
    
    switch (islem) {
        case "+": sonuc = sayi1 + sayi2; break;
        case "-": sonuc = sayi1 - sayi2; break;
        case "*": sonuc = sayi1 * sayi2; break;
        case "/": sonuc = sayi1 / sayi2; break;
    }
    
    txtEkran.setText(String.valueOf(sonuc));
    
    // Zincir bitti ama çıkan sonuçla devam etmek isterse diye sayi1'e atayalım
    sayi1 = sonuc; 
    islem = ""; // İşlemi sıfırla
    }
        
    }//GEN-LAST:event_jButton17ActionPerformed

